def merge(rel_path: str, additions: dict) -> None:
    p = TRANS / rel_path
    existing = json.loads(p.read_text(encoding="utf-8-sig"))
    before = len(existing)
    added = 0
    for k, v in additions.items():
        if k not in existing:
            existing[k] = v
            added += 1
    p.write_text(json.dumps(existing, ensure_ascii=False, indent=4) + "\n", encoding="utf-8")
    print(f"{rel_path}: {before} → +{added} → {len(existing)}")


def main() -> None:
    merge("add-on/items/zh_Hant.json", ITEMS)
    merge("add-on/materials/zh_Hant.json", MATERIALS)


if __name__ == "__main__":
    main()
