def main() -> None:
    gap = json.loads(GAP.read_text(encoding="utf-8"))
    names, descs = gap["name"], gap["description"]
    assert len(names) == len(NAME_ZH), f"name 數量不符: {len(names)} vs {len(NAME_ZH)}"
    assert len(descs) == len(DESC_ZH), f"desc 數量不符: {len(descs)} vs {len(DESC_ZH)}"

    additions = {}
    for ja, zh in zip(names, NAME_ZH):
        additions[ja] = zh
    for ja, zh in zip(descs, DESC_ZH):
        additions[ja] = zh

    existing = json.loads(TARGET.read_text(encoding="utf-8-sig"))
    before = len(existing)
    added = 0
    for k, v in additions.items():
        if k not in existing:
            existing[k] = v
            added += 1

    TARGET.write_text(
        json.dumps(existing, ensure_ascii=False, indent=4) + "\n",
        encoding="utf-8",
    )
    print(f"既有 {before} 條 → 新增 {added} 條 → 合計 {len(existing)} 條")


if __name__ == "__main__":
    main()
